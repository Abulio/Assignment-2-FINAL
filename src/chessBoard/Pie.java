package chessBoard;

public class Pie extends Shapes
{
    Integer radius;
    Integer width;
    Integer height;
    public Pie(Integer x, Integer y, Integer radius, Integer width, Integer height)
    {
        this.posX = x;
        this.posY = y;
        this.radius = radius;
        this.width = width;
        this.height = height;
    }
}
